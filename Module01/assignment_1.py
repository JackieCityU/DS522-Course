print("\nAugmented Operations of int and int\n")
a = 10
b = 5

a += b # equivalent to a = a + b
print("a=", a, "type(a):", type(a))

a -= b # equivalent to a = a - b
print("a=", a, "type(a):", type(a))

a *= b # equivalent to a = a * b
print("a=", a, "type(a):", type(a))

a /= b # equivalent to a = a / b
print("a=", a, "type(a):", type(a))

a %= b # equivalent to a = a % b
print("a=", a, "type(a):", type(a))

a **= b # equivalent to a = a ** b
print("a=", a, "type(a):", type(a))

a //= b # equivalent to a = a // b
print("a=", a, "type(a):", type(a))